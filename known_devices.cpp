// Lookup device manufacturer and product from VID/PID
void LookupKnownDevice(const std::string& vid, const std::string& pid,
                        std::string& manufacturer, std::string& product) {
    std::string upperVid = vid;
    std::string upperPid = pid;
    std::transform(upperVid.begin(), upperVid.end(), upperVid.begin(),
                   [](unsigned char c){ return (char)std::toupper(c); });
    std::transform(upperPid.begin(), upperPid.end(), upperPid.begin(),
                   [](unsigned char c){ return (char)std::toupper(c); });

    for (const KnownDevice* dev = knownDevices; dev->vid != nullptr; ++dev) {
        if (upperVid == dev->vid && upperPid == dev->pid) {
            manufacturer = dev->manufacturer;
            product = dev->product;
            return;
        }
    }
}
